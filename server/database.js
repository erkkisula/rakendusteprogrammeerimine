    const items = [];
    phones.forEach( (phone, index)=>{
        items.push({
            ...phone,
            id: "phones_" + index,
            category: "phones"
        })
    });
    laptops.forEach( (laptop, index)=>{
        items.push({
            ...laptop,
            id: "laptops_" + index,
            category: "laptops"
        })
    });
